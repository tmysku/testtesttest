#!/usr/bin/env python
#coding:utf-8

import sys
#---------------------------------------------------------------------
def plus(a, b):
    return a + b + a
#---------------------------------------------------------------------
if __name__ == '__main__':
    if plus(1,2) != 3:
        sys.exit(1)
    if plus(1.1,2.2) < 3.299999 or 3.3000001 < plus(1.1, 2.2):
        sys.exit(2)
    if plus("abc", "def") != "abcdef":
        sys.exit(3)
    sys.exit(0)
#---------------------------------------------------------------------
